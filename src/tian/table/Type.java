package tian.table;


public class Type {
    private Long id;
    private String name;
    private String imgAdr;

    public String getImgAdr() {
        return imgAdr;
    }

    public void setImgAdr(String imgAdr) {
        this.imgAdr = imgAdr;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
